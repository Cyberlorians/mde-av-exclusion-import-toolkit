<#
.SYNOPSIS
Creates one Excel-sourced Defender AV exclusions policy per platform: Windows, macOS, Linux.

.DESCRIPTION
Uses the temporary app registration created by New-MdeXdrGraphUploadApp.ps1 and the Excel-only output
files generated by New-MdeXdrDefenderAvExclusionFiles.ps1. Windows paths are packed into one pipe-
delimited Defender CSP value so Windows can be represented by one policy object.
#>
[CmdletBinding(SupportsShouldProcess)]
param(
    [string]$AppPrivatePath = ".\out\mde-xdr-upload-app-private.json",
    [string]$OutDir = ".\out",
    [ValidateSet("USGov")]
    [string]$Cloud = "USGov",
    [switch]$SkipExisting
)

Set-StrictMode -Version Latest
$ErrorActionPreference = "Stop"

$graphHost = "https://graph.microsoft.us"
$loginHost = "https://login.microsoftonline.us"

if (-not (Test-Path $AppPrivatePath)) { throw "App private file not found: $AppPrivatePath" }
$app = Get-Content $AppPrivatePath -Raw | ConvertFrom-Json
$token = Invoke-RestMethod -Method POST -Uri "$loginHost/$($app.TenantId)/oauth2/v2.0/token" -Body @{
    client_id = $app.ClientId
    client_secret = $app.ClientSecret
    scope = "$graphHost/.default"
    grant_type = "client_credentials"
}
$headers = @{ Authorization = "Bearer $($token.access_token)" }

function Invoke-LocalGraphRequest {
    param(
        [ValidateSet("GET", "POST", "DELETE")]
        [string]$Method,
        [string]$Uri,
        $Body
    )

    if ($PSBoundParameters.ContainsKey("Body")) {
        return Invoke-RestMethod -Method $Method -Uri $Uri -Headers $headers -Body ($Body | ConvertTo-Json -Depth 100) -ContentType "application/json"
    }
    return Invoke-RestMethod -Method $Method -Uri $Uri -Headers $headers
}

function Get-PipeValues {
    param([string]$Path)
    if (-not (Test-Path $Path)) { return @() }
    $raw = Get-Content $Path -Raw
    if ([string]::IsNullOrWhiteSpace($raw)) { return @() }
    return @($raw -split "\|" | ForEach-Object { $_.Trim() } | Where-Object { $_ })
}

function New-TemplateReference {
    param([string]$Id)
    [ordered]@{ settingInstanceTemplateId = $Id }
}

function New-ValueTemplateReference {
    param([string]$Id)
    [ordered]@{ settingValueTemplateId = $Id; useTemplateDefault = $false }
}

function New-StringSettingValue {

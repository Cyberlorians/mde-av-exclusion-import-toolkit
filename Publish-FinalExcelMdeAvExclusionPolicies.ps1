function New-WindowsPayload {
    $paths = Get-PipeValues -Path (Join-Path $OutDir "mde-xdr-windows-excel-master-defender-av-exclusion-paths.txt")
    $extensions = Get-PipeValues -Path (Join-Path $OutDir "mde-xdr-windows-excel-master-defender-av-exclusion-extensions.txt")
    $packedPaths = $paths -join "|"

    $settings = @(
        [ordered]@{
            "@odata.type" = "#microsoft.graph.deviceManagementConfigurationSetting"
            settingInstance = [ordered]@{
                "@odata.type" = "#microsoft.graph.deviceManagementConfigurationSimpleSettingCollectionInstance"
                settingDefinitionId = "device_vendor_msft_policy_config_defender_excludedpaths"
                settingInstanceTemplateReference = New-TemplateReference -Id "aaf04adc-c639-464f-b4a7-152e784092e8"
                simpleSettingCollectionValue = @(New-StringSettingValue -Value $packedPaths)
            }
        },
        [ordered]@{
            "@odata.type" = "#microsoft.graph.deviceManagementConfigurationSetting"
            settingInstance = [ordered]@{
                "@odata.type" = "#microsoft.graph.deviceManagementConfigurationSimpleSettingCollectionInstance"
                settingDefinitionId = "device_vendor_msft_policy_config_defender_excludedextensions"
                settingInstanceTemplateReference = New-TemplateReference -Id "c203725b-17dc-427b-9470-673a2ce9cd5e"
                simpleSettingCollectionValue = @($extensions | ForEach-Object { New-StringSettingValue -Value $_ })
            }
        }
    )

    New-PolicyPayload -Name "MDE AV Exclusions - Windows" -Description "Excel-sourced Windows Defender AV exclusions. Paths packed as one pipe-delimited Defender CSP value." -Platform "windows10" -Technology "microsoftSense" -TemplateId "45fea5e9-280d-4da1-9792-fb5736da0ca9_1" -Settings $settings
}

function New-GroupExclusionValue {
    param(
        [string]$RootPrefix,
        [string]$Type,
        [string]$Value,
        [hashtable]$Template
    )

    $isPath = $Type -eq "Path"
    $isExtension = $Type -eq "Extension"
    $isProcess = $Type -eq "Process"

    $typeOption = if ($isPath) { "$($RootPrefix)_item_`$type_0" } elseif ($isExtension) { "$($RootPrefix)_item_`$type_1" } else { "$($RootPrefix)_item_`$type_2" }
    $groupValueTemplateId = if ($isPath) { $Template.PathGroupValueTemplateId } elseif ($isExtension) { $Template.ExtensionGroupValueTemplateId } else { $Template.ProcessGroupValueTemplateId }
    $typeInstanceTemplateId = if ($isPath) { $Template.PathTypeInstanceTemplateId } elseif ($isExtension) { $Template.ExtensionTypeInstanceTemplateId } else { $Template.ProcessTypeInstanceTemplateId }
    $typeValueTemplateId = if ($isPath) { $Template.PathTypeValueTemplateId } elseif ($isExtension) { $Template.ExtensionTypeValueTemplateId } else { $Template.ProcessTypeValueTemplateId }

    $children = @()
    if ($isPath) {
        $children += New-SimpleChildInstance -DefinitionId "$($RootPrefix)_item_path" -Value $Value
        $children += New-ChoiceChildInstance -DefinitionId "$($RootPrefix)_item_isdirectory" -Value "$($RootPrefix)_item_isdirectory_true"
    }
    elseif ($isExtension) {
        $children += New-SimpleChildInstance -DefinitionId "$($RootPrefix)_item_extension" -Value $Value
    }
    elseif ($isProcess) {
        $children += New-SimpleChildInstance -DefinitionId "$($RootPrefix)_item_name" -Value $Value
    }

    [ordered]@{
        "@odata.type" = "#microsoft.graph.deviceManagementConfigurationGroupSettingValue"
        children = @(
            [ordered]@{
                "@odata.type" = "#microsoft.graph.deviceManagementConfigurationChoiceSettingInstance"
                settingDefinitionId = "$($RootPrefix)_item_`$type"
                choiceSettingValue = New-ChoiceSettingValue -Value $typeOption -ValueTemplateId $typeValueTemplateId -Children $children
            }
        )
    }
}

function New-GroupedPayload {
    param(
        [string]$Name,
        [string]$Prefix,
        [string]$Platform,
        [string]$Technology,
        [string]$TemplateId,
        [string]$RootDefinitionId,
        [string]$RootPrefix,
        [hashtable]$Template
    )

    $paths = Get-PipeValues -Path (Join-Path $OutDir "$Prefix-defender-av-exclusion-paths.txt")
    $processes = Get-PipeValues -Path (Join-Path $OutDir "$Prefix-defender-av-exclusion-processes.txt")
    $extensions = Get-PipeValues -Path (Join-Path $OutDir "$Prefix-defender-av-exclusion-extensions.txt")

    $values = @()
    $values += @($paths | ForEach-Object { New-GroupExclusionValue -RootPrefix $RootPrefix -Type "Path" -Value $_ -Template $Template })
    $values += @($extensions | ForEach-Object { New-GroupExclusionValue -RootPrefix $RootPrefix -Type "Extension" -Value $_ -Template $Template })
    $values += @($processes | ForEach-Object { New-GroupExclusionValue -RootPrefix $RootPrefix -Type "Process" -Value $_ -Template $Template })

    $settings = @(
        [ordered]@{
            "@odata.type" = "#microsoft.graph.deviceManagementConfigurationSetting"
            settingInstance = [ordered]@{
                "@odata.type" = "#microsoft.graph.deviceManagementConfigurationGroupSettingCollectionInstance"
                settingDefinitionId = $RootDefinitionId
                settingInstanceTemplateReference = New-TemplateReference -Id $Template.RootInstanceTemplateId
                groupSettingCollectionValue = @($values)
            }
        }
    )

    New-PolicyPayload -Name $Name -Description "Excel-sourced $Name Defender AV exclusions." -Platform $Platform -Technology $Technology -TemplateId $TemplateId -Settings $settings
}

function New-PolicyPayload {
    param(
        [string]$Name,
        [string]$Description,
        [string]$Platform,
        [string]$Technology,
        [string]$TemplateId,
        [object[]]$Settings
    )
    [ordered]@{
        name = $Name
        description = $Description
        platforms = $Platform
        technologies = $Technology
        roleScopeTagIds = @("0")
        templateReference = [ordered]@{
            templateId = $TemplateId
            templateFamily = "endpointSecurityAntivirus"
        }
        settings = @($Settings)
        assignments = @()
    }
}

function Publish-Policy {
    param([object]$Payload)

    $payloadPath = Join-Path $OutDir ("final-policy-{0}.json" -f (($Payload.name -replace '[^a-zA-Z0-9]+','-').Trim('-').ToLowerInvariant()))
    $Payload | ConvertTo-Json -Depth 100 | Set-Content -Path $payloadPath -Encoding UTF8 -WhatIf:$false

    $existingUri = "$graphHost/beta/deviceManagement/configurationPolicies?`$filter=name eq '$($Payload.name.Replace("'", "''"))'"
    $existing = Invoke-LocalGraphRequest -Method GET -Uri $existingUri
    if (@($existing.value).Count -gt 0) {
        if ($SkipExisting) {
            $policy = @($existing.value | Select-Object -First 1)
            return [pscustomobject]@{ Name = $policy.name; Id = $policy.id; Status = "SkippedExisting"; Payload = $payloadPath }
        }
        throw "Policy already exists: $($Payload.name). Use -SkipExisting or delete/rename it."
    }

    if ($PSCmdlet.ShouldProcess($Payload.name, "Create unassigned platform AV exclusions policy")) {
        $created = Invoke-LocalGraphRequest -Method POST -Uri "$graphHost/beta/deviceManagement/configurationPolicies" -Body $Payload
        return [pscustomobject]@{ Name = $created.name; Id = $created.id; Status = "Created"; Payload = $payloadPath }
    }

    [pscustomobject]@{ Name = $Payload.name; Id = "WHATIF"; Status = "WhatIf"; Payload = $payloadPath }
}

